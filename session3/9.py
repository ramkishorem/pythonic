"""
I'll question you one by one

Let us see how to perform the same operation on multiple 
items in a list
"""

suspects = ['David','Alice','Ben']
for suspect in suspects:
    print('Next up: {}'.format(suspect))

# Output:
# Next up: David
# Next up: Alice
# Next up: Ben

